package com.company;

public class Main {

    public static void main(String[] args) {
        for (char i = 'A'; i <= 'Z'; i++) {
            System.out.println(i + ": " + (int) i);
        }
        for (char j = 'a'; j <= 'z'; j++) {
            System.out.println(j + ": " + (int) j);
        }
    }
}
